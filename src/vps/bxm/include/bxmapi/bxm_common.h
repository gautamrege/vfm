#ifndef INCLUDED_bxm_common
#define INCLUDED_bxm_common

/**
 * @file
 * @brief Common attributes for BXM resource management API.
 */

/**
 * @brief BridgeX Manager GUID
 */
typedef uint32_t bxm_guid_t;

/**
 *@brief A bitmask to identify the writable properties of a resource
 */
typedef uint32_t bxm_component_mask_t;

/**
 * @brief Layer 2 Ethernet VLAN ID
 */
typedef uint32_t vlan_id_t;

/**
 * @brief ID of a physical module
 */
typedef uint32_t bxm_io_module_t;

/**
 * @brief 48 bit MAC Addr
 */
typedef uint8_t en_mac_addr_t[6];

/**
 * @brief 64 bit IB GUID
 */
typedef uint8_t ib_guid_t[8];

/**
 * @brief 24 bit FC-ID
 */
typedef uint8_t fc_id_t[3];

/**
 * @brief Reserved resource ID
 */
enum ResourceEnumIdEnum
{
    /**
     * @brief An invalid resource ID.
     */
    BXM_INVALID_ID	= 0,
};


/**
 * @brief List of protocol types supported on BXM
 */
typedef enum
{
    /**
     * @brief Ethernet protocol
     */
    BXM_PROTOCOL_EN	= 1,

    /**
     * @brief InfiniBand protocol
     */
    BXM_PROTOCOL_IB,
    /**
     * @brief Fibre Channel protocol
     */
    BXM_PROTOCOL_FC
}
bxm_protocol_t;

/**
 * @brief A Virtual adapter ID. The vadapter identifier is generated by the
 * system such that it can uniquely identify a vadapter object on BXM. It
 * could be an auto-increment counter.
 */
typedef uint32_t bxm_vadapter_id_t;

/**
 * @brief A virtual fabric id. This is a unique identifier for the vfabric.
 * This id is unique within a BXM context and auto-generated by the
 * system.
 */
typedef uint32_t bxm_vfabric_id_t;

/**
 * @brief A Gateway module Identifier.
 * This id is unique within a BXM context and auto-generated by the system. 
 */
typedef uint32_t bxm_gw_module_id_t;
typedef uint32_t bxm_gw_id_t;

/**
 * @brief Bridge device GUID.
 * BXM queries the GUID from the BridgeX device during initialization. The
 * bridge GUID are globally unique.
 */
typedef uint64_t bxm_bd_guid_t;

/**
 * @brief A gateway module physical index. This is a identifier for the porti
 * derived from the physical configuration of the bridge device. 
 */
typedef char *bxm_gw_module_index_t;
#endif /* INCLUDED_bxm_common */
